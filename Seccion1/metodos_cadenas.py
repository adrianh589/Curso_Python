# Metodos de cadenas
cadena1 = 'Hola mundo'
print(f'Cadena original: {cadena1}')

mayusculas = cadena1.upper() # Para convertir a mayúsculas
print(f'Cadena en mayúsculas: {mayusculas}')
print(f'Cadena en minúsculas: {cadena1.lower()}') # Convertir a minúsculas
cadena2 = '   Juan Perez   '
cadena2 = cadena2.strip()
print(f'Cadena stripeada: {cadena2}')
